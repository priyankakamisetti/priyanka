package polymorphism;

public class Calculator {

	// Add two integers
    int add(int a, int b) {
        return a + b;
    }

    // Add two doubles
    double add(double a, double b) {
        return a + b;
    }

    // Add three numbers 
    int add(int a, int b, int c) {
        return a + b + c;
    }

	
}
