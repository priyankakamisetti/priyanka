package javabitwiseoperator;

public class BitwiseOperator {
	public static void main(String args[]) {
		int a=6;
		int b=3;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		
		//program10
		
		int n = 5;
		System.out.println(n << 1);
		System.out.println(n >> 1);
		
}
}