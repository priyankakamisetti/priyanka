package javaassignmentoperator;

public class AssignmentOperator {
	public static void main(String args[]) {
		int score = 100;
		score += 15;
		System.out.println("After adding 15:" + score);
		
		score -= 10;
		System.out.println("after subtracting 10:" + score);
		
		score *= 2;
		System.out.println("after multiplying 2:" +score);
		
		
	//program12
		
	int price=500;
	//apply 10% discount(50)
	
	price -= 50;
	//add tax (25)
	price += 25;
	
	System.out.println("final price:" +price);
}
	
	

}
